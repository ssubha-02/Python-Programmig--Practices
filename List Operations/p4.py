# Append Items to a list

## Append a Single Item
my_list = [10, 20, 30]
my_list.append(40)  # Adds 40 to the end of the list

print(my_list)

##Append Another List as a Single Item

my_list = [10, 20, 30]
my_list.append([40, 50])  # Appends the whole list as one item

print(my_list)

